export function searchTrains(from, to, date) {
  const results = TRAINS.filter(
    (t) =>
      t.from.toLowerCase() === from.toLowerCase() &&
      t.to.toLowerCase() === to.toLowerCase()
  );

  // Add some randomness to seat availability
  return results.map((train) => ({
    ...train,
    date,
    seats: {
      S_CHAIR: Math.floor(Math.random() * 80) + 10,
      SNIGDHA: Math.floor(Math.random() * 40) + 5,
      F_BERTH: Math.floor(Math.random() * 20) + 2,
      AC_B: Math.floor(Math.random() * 15) + 1,
      AC_S: Math.floor(Math.random() * 20) + 5,
      F_SEAT: Math.floor(Math.random() * 30) + 5,
    },
  }));
}
